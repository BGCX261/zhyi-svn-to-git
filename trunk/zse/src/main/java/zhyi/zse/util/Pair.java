package zhyi.zse.util;

/**
 * Holds two objects. It's typically useful when a method needs to return two
 * values.
 * @param T1 Type of the first object.
 * @param T2 Type of the second object.
 * @author Zhao Yi
 */
public class Pair<T1, T2> {
    private T1 first;
    private T2 second;

    public Pair(T1 first, T2 second) {
        this.first = first;
        this.second = second;
    }

    public T1 getFirst() {
        return first;
    }

    public T2 getSecond() {
        return second;
    }
}
